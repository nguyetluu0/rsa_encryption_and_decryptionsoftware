    private void btnopenEnFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnopenEnFileActionPerformed
        // TODO add your handling code here:
        JFileChooser fChon = new JFileChooser();
        FileNameExtensionFilter fLocTxt = new FileNameExtensionFilter("txt", "txt");
        FileNameExtensionFilter fLocDoc = new FileNameExtensionFilter("docx", "docx");
        fChon.addChoosableFileFilter(fLocTxt);
        fChon.addChoosableFileFilter(fLocDoc);
        fChon.setMultiSelectionEnabled(false);
        int x = fChon.showDialog(this, "Tải file");
        if (x == JFileChooser.APPROVE_OPTION) {
            File f = fChon.getSelectedFile();
            boolean checkdocx;
            if (f.getName().contains(".txt")) {
                //           txtLinkFileMH.setText(f.getPath());
                String link=f.getPath();
                try {
                    File nd = new File(link);
                    Scanner sc = new Scanner(nd);
                    String content = "";
                    while (sc.hasNextLine()) {
                        content += sc.nextLine() + "\r\n";
                    }
                    txtInputEn.setText(content);
                    sc.close();
                    JOptionPane.showMessageDialog(this, "Tải lên File hoàn tất!");
                } catch (FileNotFoundException ex) {
                    System.out.println("Error!");
                    //                checkDocx = false;
                    JOptionPane.showMessageDialog(this, "Tải lên File thất bại!");
                }
            } else if (f.getAbsolutePath().contains(".docx")) {
                String link=f.getPath();
                try {
                    FileInputStream fis = new FileInputStream(link);
                    XWPFDocument document = new XWPFDocument(OPCPackage.open(fis));
                    XWPFWordExtractor wordExtractor = new XWPFWordExtractor(document);
                    txtInputEn.setText(wordExtractor.getText());
                    wordExtractor.close();
                    document.close();
                    //                checkDocx = true;
                    JOptionPane.showMessageDialog(this, "Tải lên File hoàn tất!");
                } catch (HeadlessException | IOException | InvalidFormatException ex) {
                    ex.printStackTrace();
                }
            }
        }
    }//GEN-LAST:event_btnopenEnFileActionPerformed

