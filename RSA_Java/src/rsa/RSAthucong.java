    private void txtTaoKhoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtTaoKhoaActionPerformed
        // TODO add your handling code here:
        
        jProgressBar1.setIndeterminate(true);
        
        String inputP = txtP.getText();
    String inputQ = txtQ.getText();

    if (inputP == null || inputP.isEmpty() || inputQ == null || inputQ.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Vui lòng nhập dữ liệu cho cả P và Q", "Thông báo", JOptionPane.ERROR_MESSAGE);
        txtP.setText("");
        txtQ.setText("");
        return; // Kết thúc sự kiện ngay lập tức
    }

    try {
        BigInteger p = new BigInteger(inputP);
        BigInteger q = new BigInteger(inputQ);

        if (!rsa.isPrime(p)) {
            JOptionPane.showMessageDialog(null, "Giá trị P không phải là số nguyên tố, vui lòng nhập lại", "Thông báo", JOptionPane.ERROR_MESSAGE);
            txtP.setText("");
            txtQ.setText("");
            return; // Kết thúc sự kiện ngay lập tức
        } else if (!rsa.isPrime(q)) {
            JOptionPane.showMessageDialog(null, "Giá trị Q không phải là số nguyên tố, vui lòng nhập lại", "Thông báo", JOptionPane.ERROR_MESSAGE);
            txtP.setText("");
            txtQ.setText("");
            return; // Kết thúc sự kiện ngay lập tức
        } else if (p.equals(q)) {
            JOptionPane.showMessageDialog(null, "Giá trị P và Q phải khác nhau, vui lòng nhập lại", "Thông báo", JOptionPane.ERROR_MESSAGE);
            txtP.setText("");
            txtQ.setText("");
            return; // Kết thúc sự kiện ngay lập tức
        } else {
            // Nếu tất cả điều kiện hợp lệ, tạo khóa RSA
            rsa.generateKeyPair(p, q);
            JOptionPane.showMessageDialog(null, "Khóa đã được tạo thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "Vui lòng nhập số hợp lệ cho P và Q", "Thông báo", JOptionPane.ERROR_MESSAGE);
        txtP.setText("");
        txtQ.setText("");
        return; // Kết thúc sự kiện ngay lập tức
    }
        



        
        
        
        
        
        
        
        txtN.setText(rsa.getN().toString());
        txtE.setText(rsa.getE().toString());
        txtphiN.setText(rsa.getphiN().toString());
        txtD.setText(rsa.getD().toString());

        jButton4.setEnabled(true);
        jButton5.setEnabled(true);

        jProgressBar1.setMaximum(100);
        jProgressBar1.setValue(200);
        jProgressBar1.setIndeterminate(false);
    }//GEN-LAST:event_txtTaoKhoaActionPerformed

